t = -1:0.001:1
x = 46
a = 1 + mod(x,3)
y1 = sin(20*a*pi*t)
y2 = cos((5*a*pi*t)+(pi/4))
y3 = exp(-2*a*t)
y4 = exp(-0.25*a*t).*sin(20*pi*t)
subplot(2,2,1)
plot(t,y1)
xlabel("t")
ylabel("y1")
subplot(2,2,2)
plot(t,y2)
xlabel("t")
ylabel("y2")
subplot(2,2,3)
plot(t,y3)
xlabel("t")
ylabel("y2")
subplot(2,2,4)
plot(t,y4)
xlabel("t")
ylabel("y4")